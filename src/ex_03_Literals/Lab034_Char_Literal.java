package ex_03_Literals;

public class Lab034_Char_Literal {

    public static void main(String[] args) {

        char new_line = '\n';
        char tab_line = '\t';
        char back_space = '\b';
        char carriage_return = '\r';

        System.out.println("Lenny"+new_line+"Ferns");
        System.out.println("Lenny\n Ferns");
        System.out.println("Lenny"+tab_line+"Ferns");
        System.out.println("Lenny"+back_space+"Ferns");
        System.out.println("Lenny"+carriage_return+"Ferns");



    }
}
